function addCompanies(companyName){
    var descriptionClass = "_1l1MA"
    var description = document.getElementsByClassName(descriptionClass)[0]
    var block1 = document.createElement("div");
    block1.style.paddingBottom = "1em";
    block1.style.display = "flex"

    for (var i = 0; i < companyName.length; i++){
        var company = createCompany(companyName[i])
        block1.append(company)
    }

    description.prepend(block1)
}

